def generate_all_docs(source_code, analysis, project_name="My Project", style="formal", language="ar", doc_types=None):
    if doc_types is None:
        doc_types = ["readme", "api", "wiki", "changelog"]
    result = {}
    if "readme" in doc_types:
        result["README.md"] = generate_readme_advanced(analysis, style, language, project_name)
    if "api" in doc_types:
        result["API_DOCS.md"] = generate_api_docs_advanced(analysis, style, language)
