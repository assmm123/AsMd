class TemplateEngine:
    @staticmethod
    def get_badge(label, message, color="blue"):
        return f"![{label}](https://img.shields.io/badge/{label}-{message}-{color})"
    
    @staticmethod
    def get_table(headers, rows):
        r = "| " + " | ".join(headers) + " |\n"
        r += "|" + "|".join(["------" for _ in headers]) + "|\n"
        for row in rows:
            r += "| " + " | ".join(str(c) for c in row) + " |\n"
        return r
    
    @staticmethod
    def get_code_block(code, language="python"):
        return f"```{language}\n{code}\n```"
    
    @staticmethod
    def get_alert(text, alert_type="info"):
        icons = {"info": "i", "warning": "!", "tip": "*", "danger": "x", "success": "+"}
        icon = icons.get(alert_type, "i")
        return f"> [{icon}] {text}"
    
    @staticmethod
    def get_section(title, content, level=2):
        prefix = "#" * level
        return f"\n{prefix} {title}\n\n{content}\n"

class QualityChecker:
    @staticmethod
    def check_readme(content):
        score = 0
        checks = []
        sections = {"description": 1.5, "installation": 1.5, "usage": 1.5, "features": 1.5, "requirements": 0.5, "license": 0.5, "contributing": 0.5, "stats": 0.5}
        cl = content.lower()
        for s, p in sections.items():
            if s in cl or s in cl:
                score += p
                checks.append(f"+ {s}")
            else:
                checks.append(f"- {s}")
        if "```" in content: score += 0.5; checks.append("+ code")
        if "|" in content: score += 0.5; checks.append("+ table")
        if len(content) > 500: score += 0.5; checks.append("+ length")
        score = min(score, 10)
        grade = "A+" if score >= 9 else "A" if score >= 8 else "B" if score >= 6 else "C" if score >= 4 else "D"
        return {"score": score, "grade": grade, "checks": checks}

