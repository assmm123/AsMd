def generate_api_docs_advanced(analysis, style="tech", language="ar"):
    functions = analysis.get("functions", [])
    classes = analysis.get("classes", [])
    lang = analysis.get("language", "Python")
    
    if language == "en":
        TXT = {"functions": "Functions", "classes": "Classes", "nofunc": "No functions", "noclass": "No classes", "line": "Line", "params": "Parameters", "returns": "Returns", "yes": "Yes", "no": "No", "desc": "Description", "gen": "Generated"}
    else:
        TXT = {"functions": "الدوال", "classes": "الكلاسات", "nofunc": "لا توجد دوال", "noclass": "لا توجد كلاسات", "line": "السطر", "params": "المعلمات", "returns": "القيمة المرجعة", "yes": "نعم", "no": "لا", "desc": "الوصف", "gen": "تم التوليد"}
    
    lines = []
    lines.append(f"# {analysis.get('filename','Project')} - API Documentation")
    lines.append(T.get_badge("Language", lang, "green"))
    lines.append(T.get_badge("Functions", str(len(functions)), "blue"))
    lines.append("---")
    
    lines.append(f"## {TXT['functions']}")
    if functions:
        for func in functions:
            lines.append(f"### `{func['name']}({', '.join(func.get('args',[]))})`")
            lines.append(f"- {TXT['line']}: {func.get('lineno','?')}")
            lines.append(f"- {TXT['returns']}: {TXT['yes'] if func.get('has_return') else TXT['no']}")
            if func.get('docstring'): lines.append(f"- {TXT['desc']}: {func['docstring']}")
            lines.append("---")
    else:
        lines.append(TXT['nofunc'])
    
    lines.append(f"## {TXT['classes']}")
    if classes:
        for cls in classes:
            lines.append(f"### class {cls['name']}")
            lines.append("---")
    else:
        lines.append(TXT['noclass'])
    
    lines.append(f"*{TXT['gen']}: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}*")
    return "\n".join(lines)

def generate_wiki(analysis, language="ar"):
    functions = analysis.get("functions", [])
    classes = analysis.get("classes", [])
    lang = analysis.get("language", "Python")
    
    if language == "en":
        T = {"title": "Project Wiki", "overview": "Overview", "functions": "Functions Guide", "classes": "Classes Guide", "usage": "Usage Examples", "getting_started": "Getting Started", "desc": "Description"}
    else:
        T = {"title": "دليل المشروع", "overview": "نظرة عامة", "functions": "دليل الدوال", "classes": "دليل الكلاسات", "usage": "أمثلة استخدام", "getting_started": "بداية سريعة", "desc": "الوصف"}
    
    lines = []
    lines.append(f"# {T['title']}")
    lines.append("")
    lines.append(f"## {T['overview']}")
    lines.append(f"هذا المشروع مكتوب بلغة **{lang}** ويحتوي على **{len(functions)}** دالة و **{len(classes)}** كلاس.")
    lines.append("")
    
    if functions:
        lines.append(f"## {T['functions']}")
        lines.append("")
        for func in functions[:20]:
            name = func.get("name", "")
            doc = func.get("docstring", "")
            desc = doc.split("\n")[0] if doc else T["desc"]
            args = ", ".join(func.get("args", []))
            lines.append(f"### `{name}({args})`")
            lines.append(f"{desc}")
            lines.append("")
    
    if classes:
        lines.append(f"## {T['classes']}")
        lines.append("")
        for cls in classes[:10]:
            name = cls.get("name", "")
            doc = cls.get("docstring", "")
            methods = cls.get("methods", [])
            lines.append(f"### `class {name}`")
            if doc: lines.append(f"{doc.split(chr(10))[0]}")
            if methods:
                lines.append(f"الطرق: {', '.join(['`'+m['name']+'()`' for m in methods])}")
            lines.append("")
    
    lines.append(f"## {T['usage']}")
    lines.append("```python")
    lines.append("import module")
    if functions:
        f = functions[0]
        lines.append(f"result = {f['name']}()")
    lines.append("```")
    lines.append("")
    lines.append(f"## {T['getting_started']}")
    lines.append("1. تثبيت المتطلبات: `pip install -r requirements.txt`")
    lines.append("2. استيراد المكتبات المطلوبة")
    lines.append("3. استخدام الدوال والكلاسات حسب الحاجة")
    
    return "\n".join(lines)

